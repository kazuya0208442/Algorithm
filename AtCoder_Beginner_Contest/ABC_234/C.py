# whileで１つずつ取り出してみるぜ。stringを直接reverseはできないので一旦listにしないといけない。stringはindexの指定もできない。
# 123 / 10 = 12 ... 3
# 12 / 10 = 1 ... 2
# 1 / 10 = 0 ... 1

K = int(input())
ans = []

while K > 0:
    value = K % 2 * 2
    ans.append(str(value))
    K //= 2

ans = reversed(ans)

print(''.join(ans))










